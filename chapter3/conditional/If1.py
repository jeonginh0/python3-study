# If1 조건문

#if-else문
print("Tell me your age?")
myage = int(input())
if myage < 30:
    print("Welcome to the Club")
else:
    print("Oh! No. You are not accepted")

#비교연산자
# x < y
# x > y
# x == y
# x is y (메모리주소가 같음)
# x != y
# x is not y (메모리주소가 같지 않음)
# x >= y
# x <= y

#논리연산자
# and : 두 값이 모두 참일 경우 True, 그렇지 않을 경우 False
# or : 두 값중 하나만 참일 경우 True, 두 값 모두 거짓일 경우 False
# not : 값을 역으로 반환하여 판단