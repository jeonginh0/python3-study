# Var4 자료형 변환

#정수형과 실수형간 변환 float()
a = 10
print(a)

a = float(10)
print(a)

a = 10
b = 3
print(a / b)

#정수형과 실수형간 변환 int()
a = int(10.7)
b = int(10.3)

print(a + b)
print(a)
print(b)

#숫자형과 문자형간 변환 str()
a = '76.3'
b = float(a)
print(a)
print(b)

a = float(a)
b = float(b)
print(a + b)

a = str(a)
b = str(b)
print(a + b) # 문자열 간 덧셈은 단순 연결
